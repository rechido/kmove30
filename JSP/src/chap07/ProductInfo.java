package chap07;

public class ProductInfo {
	
	private String name;
	private int value;

	public ProductInfo() {
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getPrice() {
		return value;
	}

	public void setPrice(int price) {
		this.value = price;
	}

}
