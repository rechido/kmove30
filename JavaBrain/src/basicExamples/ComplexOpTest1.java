package basicExamples;

public class ComplexOpTest1 {
	public static void main(String[] args) {
		
		int num = 29;
		num -= 2 + 3 * 4;
		// num = num - (2+3*4);
		
		System.out.println(num);
		
		num += 5;
		System.out.println(num);
		
		num *= 4;
		System.out.println(num);
		
		num /= 3;
		System.out.println(num);
		
		num %= 7;
		System.out.println(num);
		
	}

}
